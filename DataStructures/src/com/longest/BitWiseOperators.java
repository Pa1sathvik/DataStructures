package com.longest;

public class BitWiseOperators {

	
	public static void main(String args[]) {
		
		
		int a = 10;
		int b = 12;
		
		System.out.println(a | b);
		System.out.println(a & b);
		System.out.println(a ^ b);
		System.out.println(~a);
		
		int left = 1;
		//0001
		System.out.println(left << 1);

		
	}
}
