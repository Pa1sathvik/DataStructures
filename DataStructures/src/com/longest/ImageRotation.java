package com.longest;

public class ImageRotation {

	public static void main(String[] args) {

		int[][] image = { { 1, 2 }, { 4, 5},{7,8} };
		
		
		for (int i = 0; i < image.length; i++) {

			
			for (int j = 0; j < image[i].length; j++) {
				
				System.out.println(image[i][j]);

			}

		}

	}

}
